        <p><strong>Diplôme de la Fédération Européenne des Ecoles (F.E.D.E)</strong></p>
        <p>La Fédération Européenne des Ecoles fédère 450 établissements d’enseignement situés dans les pays membres du
          conseil de l’Europe. Elle conçoit, pilote, homologue et délivre des diplômes au niveau Européen.</p>
        <p>Le DEESWEB (DEES Webmaster) permet aux étudiants titulaires d'un Bac +2 dans le domaine de l'informatique
          (DT, DTS, Licence 2, DEUG, …) d'acquérir des connaissances supplémentaires dans les domaines de la gestion de
          sites ou de serveurs Internet et dans l'administration des réseaux.</p>
        <p>Le travail d'un WEBMASTER est souvent confondu avec celui d'un développeur. Il est clair que les compétences
          d'un développeur font partie des capacités d'un WEBMASTER mais la différence est que le WEBMASTER est un
          créateur, capable de concevoir des sites de A à Z et de livrer à son client un produit fini, adaptés à ses
          besoins personnels.</p>
        <p><b>Ce n'est pas un exécutant mais c'est véritable partenaire</b> : Il conseille, explique au client
          Il est responsable non seulement du succès du site mais aussi de l'image que l'entreprise de son client
          présente sur le Net, c’est-à-dire devant le monde entier.</p>
        <p><b>Avant de se lancer dans la création, le futur WEBMASTER doit maîtriser ses outils</b> : c’est-à-dire la
          programmation Internet : Langages du WEB, PHP, MySQL, XML, Perl, JavaScript</p>
        <p>La formation WEBMASTER familiarise les étudiants avec les institutions du WEB, le fonctionnement des moteurs
          de recherche, du système des noms sur Internet ou celui des différents types de serveurs.</p>
        <p>Le WEBMASTER, maître du Net étudie les questions du graphisme et la rédaction du contenu du site. La maîtrise
          des logiciels comme, entre autres, FLASH ou PHOTOSHOP est l'un des objectifs essentiels de cette phase de la
          formation WEBMASTER, aussi important que l'apprentissage des règles du WEB DESIGN qui font d'un site
          techniquement correct une oeuvre véritablement PRO. Le WEBMASTER est consacré à la sécurité des serveurs et à
          la sécurité sur Internet.</p>
